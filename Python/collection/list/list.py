# declaration
languages = ["Python", "Scala", "Haskell", "F#"]
print(languages)

# iterate through the list
for i in languages:
    print(i, end=' -- ')
print(' ')

# declaration using list constructor
name = list("PYTHON")
print(name)

# multiline declaration 
languages = ['Python',
           'Scala',
           'Haskell',
           'F#']
print (languages)

# list length
print("number of chars in PYTHON : {0}".format(len(list("PYTHON"))))

