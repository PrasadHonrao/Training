# Declaration
message1 = 'python ' + 'language '
print(message1)

# Multiline string
message2 = 'python '
message2 += 'language '
print(message2)

# Multiline string using '''
message3 = '''python
           language '''
print(message3)

# String comparison
print(message1 == message2)
print(message1 is message2)

# Adding escape sequence
escape = 'Escape Sequence Character \' !!!'
print(escape)
